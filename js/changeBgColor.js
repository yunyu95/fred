function changeBgColor() {
    let color = document.getElementById('color').value;
    if (color === 'yellow') {
        document.bgColor = 'yellow';
    }
    if (color === 'blue') {
        document.bgColor = 'blue';
    }
    if (color === 'red') {
        document.bgColor = 'red';
    }
    if (color === 'green') {
        document.bgColor = 'green';
    }
    if (color === 'black') {
        document.bgColor = 'black';
    }

}